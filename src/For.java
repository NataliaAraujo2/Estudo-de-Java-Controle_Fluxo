public class For {
    private int numero;

    public For() {
        this.numero = numero;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }


    public void simplesFor(int numero) {
        for (int i=0; i <= numero; i++) {
            System.out.println(i);
        }
    }
}

